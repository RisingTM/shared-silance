export function isoYearWeek(d: Date): { key: string; question: string } {
  const date = new Date(Date.UTC(d.getFullYear(), d.getMonth(), d.getDate()));
  const dayNum = (date.getUTCDay() + 6) % 7;
  date.setUTCDate(date.getUTCDate() - dayNum + 3);
  const firstThursday = new Date(Date.UTC(date.getUTCFullYear(), 0, 4));
  const week = 1 + Math.round(((date.getTime() - firstThursday.getTime()) / 86400000 - 3 + ((firstThursday.getUTCDay() + 6) % 7)) / 7);
  const key = `${date.getUTCFullYear()}-W${String(week).padStart(2, "0")}`;
  const idx = (date.getUTCFullYear() * 53 + week) % REFLECTION_QUESTIONS.length;
  return { key, question: REFLECTION_QUESTIONS[idx] };
}
